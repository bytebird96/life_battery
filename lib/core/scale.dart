import 'package:flutter/widgets.dart';

// --------------------------------------------------------------
// 화면 크기에 맞춰 위젯의 크기를 비율로 계산하는 헬퍼 함수
// --------------------------------------------------------------
// 디자인 기준 기기(iPhone 11 Pro 등)의 너비는 375으로 가정한다.
const double _kBaseW = 375.0;

/// [v]에 전달한 값을 현재 화면 너비에 맞춰 스케일링한다.
///
/// 예를 들어 디자인에서 20px로 지정된 간격을 그대로 전달하면
/// 작은 기기에서는 더 작게, 큰 기기에서는 더 크게 자동 조정된다.
///
/// [c] : BuildContext - MediaQuery를 얻기 위한 컨텍스트
/// [v] : 디자인 기준 값
/// 반환 : 실제 디바이스에서 사용할 값
double s(BuildContext c, double v) => MediaQuery.of(c).size.width / _kBaseW * v;
